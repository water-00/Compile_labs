StoreInstruction::StoreInstruction(Operand *dst_addr, Operand *src, BasicBlock *insert_bb) : Instruction(STORE, insert_bb)
{
    // 在output()中会把src存到dst中（仅仅是输出代码，并没有真的执行这一步操作）
    operands.push_back(dst_addr);
    operands.push_back(src);
    dst_addr->addUse(this);
    src->addUse(this);
}

StoreInstruction::~StoreInstruction()
{
    operands[0]->removeUse(this);
    operands[1]->removeUse(this);
}

void StoreInstruction::output() const
{
    // std::cout << "StoreInstruction::output()\n";
    std::string dst = operands[0]->toStr();
    std::string src = operands[1]->toStr();
    std::string dst_type = operands[0]->getType()->toStr();
    std::string src_type = operands[1]->getType()->toStr();

    fprintf(yyout, "  store %s %s, %s %s, align 4\n", src_type.c_str(), src.c_str(), dst_type.c_str(), dst.c_str());
}

CallInstruction::CallInstruction(Operand *dst, SymbolEntry *se, std::vector<Operand*> params, BasicBlock* insert_bb) : Instruction(CALL, insert_bb) {
    this->func = se;
    operands.push_back(dst); // 放函数返回值的操作数
    if (dst != nullptr) {
        dst->setDef(this);
    }
    for (auto param : params) {
        operands.push_back(param); // 放实参
        param->addUse(this);
    }
}

